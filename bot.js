function formatStudentInfo(data) {
  const student = data.student
  let message = `‚úÖ O'quvchi ma'lumotlari:\n\n`
  message += `üë§ Ism: ${student.user?.fullName || "Ma'lumot yo'q"}\n`
  message += `üÜî Kod: ${student.studentCode}\n`
  message += `üìö Guruh: ${student.group?.name || "Ma'lumot yo'q"}\n`
  message += `üìä Holat: ${getStatusEmoji(student.status)} ${getStatusText(student.status)}\n`
  message += `üí∞ Qarz: ${(student.totalDebt || 0).toLocaleString()} so'm\n\n`

  if (student.group?.schedule && student.group.schedule.length > 0) {
    message += formatSchedule(student.group.schedule) + "\n\n"
  }

  return message
}

function formatSchedule(schedule) {
  if (!schedule || schedule.length === 0) {
    return "üìÖ Dars jadvali ma'lumotlari yo'q"
  }

  const days = ["Yakshanba", "Dushanba", "Seshanba", "Chorshanba", "Payshanba", "Juma", "Shanba"]
  let result = "üìÖ Dars jadvali:\n\n"

  schedule.forEach((item) => {
    result += `üìö ${days[item.dayOfWeek]}: ${item.startTime} - ${item.endTime}\n`
  })

  return result
}

